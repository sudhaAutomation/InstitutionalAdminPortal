package com.admin.qa.domain;

public class CourseDetails {
	private String photo;
	private String category;
	private String name;
	private String url;
	private String content;
	private String broucher;
	private String videoURL;
	private String isPopular;
	private String sortOrder;

	public String getPhoto() {
		return photo;
	}

	public void setPhoto(String photo) {
		this.photo = photo;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getBroucher() {
		return broucher;
	}

	public void setBroucher(String broucher) {
		this.broucher = broucher;
	}

	public String getVideoURL() {
		return videoURL;
	}

	public void setVideoURL(String videoURL) {
		this.videoURL = videoURL;
	}

	public String getIsPopular() {
		return isPopular;
	}

	public void setIsPopular(String isPopular) {
		this.isPopular = isPopular;
	}

	public String getSortOrder() {
		return sortOrder;
	}

	public void setSortOrder(String sortOrder) {
		this.sortOrder = sortOrder;
	}

}
